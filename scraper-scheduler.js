const schedule = require('node-schedule');
const { runScraper } = require('./scraper-master');
const fs = require('fs');

const LOG_FILE = './scraper-logs.json';

// Load log history
function loadLogs() {
    try {
        if (fs.existsSync(LOG_FILE)) {
            return JSON.parse(fs.readFileSync(LOG_FILE, 'utf-8'));
        }
    } catch (e) {
        console.error('Error loading logs:', e.message);
    }
    return [];
}

// Save log
function saveLogs(logs) {
    fs.writeFileSync(LOG_FILE, JSON.stringify(logs, null, 2));
}

// Run scheduled scraper
async function scheduledRun() {
    console.log('\nрџ“… Scheduled scraper run started...');
    const result = await runScraper();

    // Log the result
    const logs = loadLogs();
    logs.push({
        timestamp: new Date().toISOString(),
        success: result.success,
        total: result.total || 0,
        newCount: result.newCount || 0,
        error: result.error || null
    });

    // Keep only last 30 days
    const thirtyDaysAgo = new Date();
    thirtyDaysAgo.setDate(thirtyDaysAgo.getDate() - 30);
    const filtered = logs.filter(l => new Date(l.timestamp) > thirtyDaysAgo);

    saveLogs(filtered);

    console.log(`рџ“Љ Log saved. Total logs: ${filtered.length}`);
}

